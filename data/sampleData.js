function createProfile(student) {
  const semester =
    student.program === "mtech" ? student.year * 2 : student.year * 2;
  const semesterMarks = [];

  for (
    let semesterNumber = Math.max(1, semester - 2);
    semesterNumber <= semester;
    semesterNumber += 1
  ) {
    semesterMarks.push({
      semester: String(semesterNumber),
      marksObtained: String(725 + semesterNumber * 8),
      totalMarks: "800",
      percentage: String((86 + semesterNumber * 0.8).toFixed(1)),
      cgpa: String(
        Math.min(10, student.cgpa - 0.25 + semesterNumber * 0.04).toFixed(2),
      ),
    });
  }

  return {
    basic: {
      name: student.name,
      email: student.email,
      phone: student.phone,
      address: `${student.city}, Maharashtra, India`,
      linkedin: student.linkedin,
      portfolio: student.portfolio,
    },
    academic: {
      currentInstitute: COMMON_INSTITUTE,
      department: student.branch,
      currentSemester: String(semester),
      cgpa: String(student.cgpa.toFixed(2)),
    },
    semesterMarks,
    education: [
      {
        qualification: "10th",
        institute: `${COMMON_SCHOOL}, ${student.city}`,
        boardOrUniversity: "CBSE",
        yearOfPassing: "2018",
        scoreType: "Percentage",
        score: "94.2",
      },
      {
        qualification: "12th",
        institute: `${COMMON_SCHOOL}, ${student.city}`,
        boardOrUniversity: "Maharashtra State Board",
        yearOfPassing: "2020",
        scoreType: "Percentage",
        score: String(student.class12Percentage.toFixed(1)),
      },
      {
        qualification: student.program === "mtech" ? "M.Tech" : "B.Tech",
        institute: COMMON_INSTITUTE,
        boardOrUniversity: COMMON_UNIVERSITY,
        yearOfPassing: student.program === "mtech" ? "2026" : "2026",
        scoreType: "CGPA",
        score: String(student.cgpa.toFixed(2)),
      },
    ],
    experiences: [
      {
        type: "internship",
        organization: student.internshipCompany,
        role: student.internshipRole,
        startDate: "2025-05-01",
        endDate: "2025-07-31",
        duration: "3 months",
        description: `Built production features for ${student.internshipCompany} and shipped internship deliverables on time.`,
      },
    ],
    projects: [student.projectOne, student.projectTwo],
    skills: student.skills,
    certifications: [student.certification],
    achievements: [student.achievement],
    extracurriculars: [student.extracurricular],
  };
}

function createStudent(student) {
  return {
    key: student.key,
    name: student.name,
    email: student.email,
    password: "Student@123",
    role: "student",
    branch: student.branch,
    program: student.program,
    year: student.year,
    isProfileVerified: true,
    profileVerifiedAt: student.profileVerifiedAt || daysAgo(12),
    profileVerifiedByKey: student.profileVerifiedByKey,
    isBanned: Boolean(student.isBanned),
    banReason: student.banReason || "",
    bannedAt: student.isBanned ? student.bannedAt || daysAgo(20) : null,
    isDismissed: Boolean(student.isDismissed),
    dismissalReason: student.dismissalReason || "",
    dismissedAt: student.isDismissed
      ? student.dismissedAt || daysAgo(18)
      : null,
    dismissedByKey: student.dismissedByKey || null,
    phone: student.phone,
    city: student.city,
    linkedin: student.linkedin,
    portfolio: student.portfolio,
    profile: createProfile(student),
  };
}

